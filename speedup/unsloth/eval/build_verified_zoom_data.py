"""
Add crop-verification to zoom_json SFT data.

For every sample:
  - Keep the GT bbox as a POSITIVE example (crop is correct → confirm → answer)
  - Generate a SHIFTED bbox as a NEGATIVE example (crop is wrong → detect → recover)

The negative bbox is generated by shifting the GT bbox by +-30% of image size
in a random direction. The stage_2_reasoning for negatives includes:
  1. What the crop ACTUALLY shows
  2. Explicitly stating the zoom is wrong
  3. Describing where the target should have been
  4. Answering based on global context

Output: verified_zoom.jsonl with ~2x samples (pos + neg)

Usage:
  python build_verified_zoom_data.py
"""

import json
import random
import re
from copy import deepcopy
from pathlib import Path
from typing import Optional

VQA_ROOT = Path("/root/autodl-tmp/VQA")
INPUT = VQA_ROOT / "json_data/zoom_json/sft-00000-of-00001.jsonl"
OUTPUT = VQA_ROOT / "json_data/zoom_json/verified_zoom_sft.jsonl"

random.seed(42)

# ── Labels for negative description ─────────────────────────────────────
# When the model zooms to the wrong place, we need to describe what's ACTUALLY
# in the shifted area. These descriptions help the model learn to identify
# what it's looking at vs what it SHOULD be looking at.

LAND_COVER_CLUES = [
    "open water with faint wave patterns",
    "a paved road surface with lane markings",
    "dense tree canopy and vegetation",
    "bare soil or exposed ground",
    "a cluster of industrial buildings with grey roofs",
    "a residential area with small houses",
    "an open field with uniform texture",
    "a parking lot with scattered vehicles",
    "a dock or pier structure extending into water",
    "a patch of agricultural land with crop rows",
    "shadows cast by tall structures",
    "a section of highway with multiple lanes",
    "a container yard with stacked shipping containers",
    "a runway or taxiway surface",
    "a river or canal crossing",
]


def shift_bbox(bbox: list, label: str, global_desc: str) -> dict:
    """Create a wrong bbox by shifting the GT bbox.

    The shift keeps the bbox within [0, 1023] and moves it by 15-40% of
    the 1024 space in a random direction.
    """
    x1, y1, x2, y2 = bbox
    bw, bh = x2 - x1, y2 - y1

    # Shift amount: enough to miss the target, not so much it's off-image
    shift_x = int(random.uniform(0.5, 1.2) * max(bw, 64) * random.choice([-1, 1]))
    shift_y = int(random.uniform(0.5, 1.2) * max(bh, 64) * random.choice([-1, 1]))

    nx1 = max(0, min(1023 - bw, x1 + shift_x))
    ny1 = max(0, min(1023 - bh, y1 + shift_y))
    nx2 = nx1 + bw
    ny2 = ny1 + bh

    # Ensure the shifted bbox doesn't overlap with GT (IoU < 0.1)
    # Simple check: if shift is small relative to bbox size, increase it
    def iou(a, b):
        xa1, ya1, xa2, ya2 = a
        xb1, yb1, xb2, yb2 = b
        ix1, iy1 = max(xa1, xb1), max(ya1, yb1)
        ix2, iy2 = min(xa2, xb2), min(ya2, yb2)
        if ix2 <= ix1 or iy2 <= iy1:
            return 0.0
        inter = (ix2 - ix1) * (iy2 - iy1)
        aa = (xa2 - xa1) * (ya2 - ya1)
        ab = (xb2 - xb1) * (yb2 - yb1)
        return inter / (aa + ab - inter + 1e-9)

    if iou(bbox, [nx1, ny1, nx2, ny2]) > 0.15:
        # Push harder — move to opposite corner
        nx1 = max(0, min(1023 - bw, x1 + int(random.choice([-1, 1]) * max(bw, 200) * 1.5)))
        ny1 = max(0, min(1023 - bh, y1 + int(random.choice([-1, 1]) * max(bh, 200) * 1.5)))
        nx2 = nx1 + bw
        ny2 = ny1 + bh

    return [nx1, ny1, nx2, ny2]


def build_verification_positive(stage_2: str, label: str, gt_answer: str) -> str:
    """Add verification prefix to a correct stage_2."""
    verify = (
        f"\n\n[Crop Verification] Looking at the cropped region, I can clearly "
        f'identify the "{label}" — the visual features match the referent. '
        f"The zoom is accurate: I am looking at exactly the right area.\n"
        f"\n[Analysis] {stage_2.strip()}\n"
    )
    return verify


def build_verification_negative(
    stage_2: str, label: str, gt_answer: str,
    shifted_bbox: list, global_desc: str
) -> str:
    """Build a negative verification: crop doesn't contain the target."""
    clue = random.choice(LAND_COVER_CLUES)

    # Generate a natural-sounding description of what the wrong crop shows
    wrong_descs = [
        f"The cropped region shows {clue}, along with some surrounding terrain. "
        f'However, this area clearly does not contain the "{label}" I intended to examine. '
        f"The zoom is off-target.",

        f"Examining the crop, I see {clue}. "
        f'This does not match the expected appearance of "{label}". '
        f"I have zoomed to the wrong location.",

        f"The crop reveals {clue}, not the intended target. "
        f'Looking at this area, I cannot find "{label}" — the zoom missed.',
    ]
    wrong_desc = random.choice(wrong_descs)

    verify = (
        f"\n\n[Crop Verification] {wrong_desc}\n"
        f"\n[Correction] Since the cropped region does not contain the target, "
        f"I should not force an analysis of unrelated content. "
        f"Instead, I will answer based on what I already observed in the global "
        f"image context:\n{global_desc[:300].strip()}\n"
        f"\n[Answer Based on Global Context] {stage_2.strip()}\n"
    )

    return verify


def build_assistant_text(
    global_desc: str,
    stage_1: str,
    stage_2: str,
    bbox: list,
    label: str,
    gt_answer: str,
    is_negative: bool = False,
    global_full: str = "",
) -> str:
    """Build the full assistant_text in zoom format."""

    # Extract the "Reasoning Result" part from stage_1
    bbox_json = json.dumps([{"bbox_2d": bbox, "label": label}])

    think_1 = (
        f"{global_desc}\n\n{stage_1.strip()}\n\n"
        f"I need to pay attention to the reference object at\n"
    )

    # Build stage 2
    if is_negative:
        think_2 = build_verification_negative(stage_2, label, gt_answer, bbox, global_desc)
    else:
        think_2 = build_verification_positive(stage_2, label, gt_answer)

    return (
        f"<think>{think_1}</think>\n"
        f"<zoom>{bbox_json}</zoom>\n"
        f"<think>{think_2}</think>\n"
        f"<answer>{gt_answer}</answer>"
    )


def main():
    with open(INPUT) as f:
        samples = [json.loads(line) for line in f if line.strip()]

    print(f"Loaded {len(samples)} samples from zoom_json")

    new_samples = []

    for s in samples:
        bbox = s.get("bbox", [])
        label = s.get("label", "")
        gt_answer = s.get("ground_truth", "")
        stage_1 = s.get("stage_1_reasoning", "")
        stage_2 = s.get("stage_2_reasoning", "")
        global_desc = s.get("global", "")

        # Global samples (no bbox) — pass through unchanged
        if not bbox or len(bbox) != 4:
            s2 = deepcopy(s)
            s2["bbox_correct"] = True
            s2["assistant_text"] = (
                f"<think>{global_desc}\n\n{stage_1.strip()}\n\n"
                f"{stage_2.strip()}</think>\n"
                f"<answer>{gt_answer}</answer>"
            )
            new_samples.append(s2)
            continue

        # ── Positive sample ──
        pos = deepcopy(s)
        pos["assistant_text"] = build_assistant_text(
            global_desc, stage_1, stage_2, bbox, label, gt_answer,
            is_negative=False,
        )
        pos["bbox_correct"] = True
        new_samples.append(pos)

        # ── Negative sample ──
        shifted = shift_bbox(bbox, label, global_desc)

        neg = deepcopy(s)
        neg["bbox"] = shifted
        neg["bbox_correct"] = False
        neg["assistant_text"] = build_assistant_text(
            global_desc, stage_1, stage_2, shifted, label, gt_answer,
            is_negative=True, global_full=global_desc,
        )
        new_samples.append(neg)

    # Shuffle so pos/neg are interleaved
    random.shuffle(new_samples)

    with open(OUTPUT, "w", encoding="utf-8") as f:
        for s in new_samples:
            f.write(json.dumps(s, ensure_ascii=False) + "\n")

    n_pos = sum(1 for s in new_samples if s.get("bbox_correct"))
    n_neg = sum(1 for s in new_samples if not s.get("bbox_correct"))
    print(f"Written {len(new_samples)} samples to {OUTPUT}")
    print(f"  Positive (correct crop): {n_pos}")
    print(f"  Negative (wrong crop):   {n_neg}")

    # Show a sample
    print("\n=== Positive example ===")
    pos_ex = next(s for s in new_samples if s.get("bbox_correct"))
    print(pos_ex["assistant_text"][:600])
    print("...\n")

    print("=== Negative example ===")
    neg_ex = next(s for s in new_samples if not s.get("bbox_correct"))
    print(neg_ex["assistant_text"][:600])
    print("...")


if __name__ == "__main__":
    main()
